    public static List<List<float[]>> GroupEmbeddings(double threshold) {
	    var groups = new List<List<float[]>>();

	    var embeddings = sentences.Select(s => s.Embedding.Select(e => (float)e).ToArray());
	    
	    foreach (var embedding in embeddings) {
		    bool isGrouped = false;
		    foreach (var group in groups) {
			    if (IsSimilarToGroup(embedding, group, threshold)) {
				    group.Add(embedding);
				    isGrouped = true;
				    break;
			    }
		    }
		    if (!isGrouped) {
			    groups.Add(new List<float[]> { embedding });
		    }
	    }

	    return groups;
    }

    public static bool IsSimilarToGroup(float[] embedding, List<float[]> group, double threshold) {
	    foreach (var member in group) {
		    if (CosineSimilarity(embedding, member) > threshold) {
			    return true;
		    }
	    }
	    return false;
    }

    public static double CosineSimilarity(float[] A, float[] B)
    {
	    return TensorPrimitives.CosineSimilarity(A, B);
    }
